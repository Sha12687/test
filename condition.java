public class condition {
    public static void main(String[] args) {
        boolean a=true;
        boolean b=false;
        System.out.println("For  logical AND  operator");

        if(a && b){
            System.out.println("Yes");
        }
        else{
            System.out.println("No");
        }
        System.out.println("For  logical OR operator");
        boolean x=true;
        boolean y=false;
        if(y || x){
            System.out.println("Yes");
        }
        else{
            System.out.println("No");
        }
    }

}
